package Tarzan;

public class Arista {

	public int getNo() {
		return no;
	}

	public int getNd() {
		return nd;
	}

	public int getValue() {
		return value;
	}

	private int no;
	private int nd;
	private int value;

	public Arista(int no, int nd, int value) {
		super();
		this.no = no;
		this.nd = nd;
		this.value = value;
	}
}
